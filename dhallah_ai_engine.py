from io import BytesIO

def extract_image_features(image_url):
    if not image_url:
        return None

    try:
        response = requests.get(image_url, timeout=10)
        if response.status_code != 200:
            print(f"Failed to fetch image. Status: {response.status_code}")
            return None

        image = Image.open(BytesIO(response.content)).convert('RGB')
        inputs = image_processor(images=image, return_tensors="pt").to(device)

        with torch.no_grad():
            outputs = image_model(**inputs)
            # Extract CLS token (first token of the last hidden state)
            embedding = outputs.last_hidden_state[:, 0, :].cpu().numpy()[0]

        # L2 Normalization for Cosine Similarity
        normalized_embedding = embedding / np.linalg.norm(embedding)
        return normalized_embedding.tolist()

    except Exception as e:
        print(f"Image extraction error: {e}")
        return None

def build_rich_text(item_dict):
    if not isinstance(item_dict, dict):
        return ""

    parts = []
    # Order matters slightly for context: Category -> Color -> Description -> Features
    if 'category' in item_dict and item_dict['category']:
        parts.append(item_dict['category'])
    if 'color' in item_dict and item_dict['color']:
        parts.append(item_dict['color'])
    if 'description' in item_dict and item_dict['description']:
        parts.append(item_dict['description'])
    if 'uniqueFeatures' in item_dict and item_dict['uniqueFeatures']:
        parts.append(item_dict['uniqueFeatures'])

    return " ".join(parts)

def extract_text_features(text_input):
    if not text_input or text_input.strip() == "":
        return None

    try:
        # MiniLM supports built-in normalization
        vector = text_model.encode(text_input, normalize_embeddings=True)
        return vector.tolist()
    except Exception as e:
        print(f"Text extraction error: {e}")
        return None

