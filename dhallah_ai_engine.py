import uuid
from qdrant_client.models import PointStruct, Filter, FieldCondition, MatchValue

def generate_qdrant_id(firebase_id):
    return str(uuid.uuid5(uuid.NAMESPACE_OID, firebase_id))

# Updated indexing function to include 'status' in the payload
def index_report_to_qdrant(firebase_id, item_dict, report_type, user_id, report_status="open"):
    qdrant_id = generate_qdrant_id(firebase_id)

    image_url = item_dict.get('imageURL')
    img_vector = extract_image_features(image_url) if image_url else None

    rich_text = build_rich_text(item_dict)
    text_vector = extract_text_features(rich_text)

    vectors_dict = {}
    if img_vector:
        vectors_dict["image"] = img_vector
    if text_vector:
        vectors_dict["text"] = text_vector

    if not img_vector:
        print(f"[QDRANT] Skipped {firebase_id}: Image vector is mandatory for Dhallah.")
        return False

    client.upsert(
        collection_name=collection_name,
        points=[
            PointStruct(
                id=qdrant_id,
                vector=vectors_dict,
                payload={
                    "firebase_id": firebase_id,
                    "type": report_type,
                    "userId": user_id,
                    "has_image": bool(img_vector),
                    "status": report_status # updated payload to include status
                }
            )
        ]
    )
    print(f"[QDRANT] Successfully indexed {firebase_id} ({report_type}).")
    return True
# End of update

