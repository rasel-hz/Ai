from qdrant_client.models import Filter, FieldCondition, MatchValue

def search_weighted_matches(item_dict, current_type, current_user_id, top_k=3):
    target_type = "found" if current_type == "lost" else "lost"

    # Updated filter to include only open reports of the opposite type
    must_conditions = [
        FieldCondition(key="type", match=MatchValue(value=target_type)),
        FieldCondition(key="status", match=MatchValue(value="open"))
    ]
    # end of update

    must_not_conditions = []
    if current_user_id and current_user_id != 'unknown':
        must_not_conditions.append(
            FieldCondition(key="userId", match=MatchValue(value=current_user_id))
        )

    type_filter = Filter(
        must=must_conditions,
        must_not=must_not_conditions
    )

    image_url = item_dict.get('imageURL')
    query_img_vector = extract_image_features(image_url) if image_url else None

    rich_text = build_rich_text(item_dict)
    query_txt_vector = extract_text_features(rich_text)

    combined_scores = {}
    use_query_points = hasattr(client, 'query_points')

    print(f"[SEARCH] Querying Qdrant for opposite type: {target_type}...")

    if query_img_vector and query_txt_vector:
        if use_query_points:
            img_hits = client.query_points(collection_name=collection_name, query=query_img_vector, using="image", query_filter=type_filter, limit=10).points
        else:
            img_hits = client.search(collection_name=collection_name, query_vector=("image", query_img_vector), query_filter=type_filter, limit=10)

        for hit in img_hits:
            fb_id = hit.payload['firebase_id']
            weighted_img_score = hit.score * 0.8
            combined_scores[fb_id] = combined_scores.get(fb_id, 0) + (hit.score * 0.8)
            print(f"{Fore.CYAN}[DEBUG] Image Match for {fb_id}: Raw={hit.score:.2f}, Weighted(80%)={weighted_img_score:.2f}")
        if use_query_points:
            txt_hits = client.query_points(collection_name=collection_name, query=query_txt_vector, using="text", query_filter=type_filter, limit=10).points
        else:
            txt_hits = client.search(collection_name=collection_name, query_vector=("text", query_txt_vector), query_filter=type_filter, limit=10)

        for hit in txt_hits:
            fb_id = hit.payload['firebase_id']
            weighted_txt_score = hit.score * 0.2
            combined_scores[fb_id] = combined_scores.get(fb_id, 0) + (hit.score * 0.2)
            print(f"{Fore.MAGENTA}[DEBUG] Text Match for {fb_id}: Raw={hit.score:.2f}, Weighted(20%)={weighted_txt_score:.2f}")
    sorted_results = sorted(combined_scores.items(), key=lambda item: item[1], reverse=True)[:top_k]

    formatted_results = [
        {"matched_report_id": fb_id, "score": round(score, 4)}
        for fb_id, score in sorted_results
    ]

    print(f"[SEARCH] Found {len(formatted_results)} matches from Qdrant.")
    return formatted_results

