from google.cloud.firestore_v1.watch import ChangeType
from colorama import Fore, Style, init

def on_snapshot(col_snapshot, changes, read_time):
    for change in changes:
        doc = change.document
        doc_id = doc.id
        data = doc.to_dict() or {}

        # --- Handle Deletion ---
        if change.type == ChangeType.REMOVED:
            print(f"{Fore.YELLOW}\n[EVENT] Report Deleted | ID: {doc_id}")
            try:
                qdrant_id = generate_qdrant_id(doc_id)
                client.delete(collection_name=collection_name, points_selector=[qdrant_id])
                print(f"{Fore.YELLOW}[QDRANT] Successfully removed {doc_id} to prevent ghost matches.")

                # Start of update: Reverse cleanup from other reports that matched with the deleted report
                old_matches_in_deleted = data.get('matches', [])
                for match in old_matches_in_deleted:
                    matched_id = match.get('matched_report_id')
                    if matched_id:
                        matched_ref = db.collection('reports').document(matched_id)
                        matched_snap = matched_ref.get()
                        if matched_snap.exists:
                            matched_data = matched_snap.to_dict()
                            matched_matches = matched_data.get('matches', [])
                            filtered = [m for m in matched_matches if m.get('matched_report_id') != doc_id]
                            if len(filtered) != len(matched_matches):
                                matched_ref.update({'matches': filtered})
                                print(f"{Fore.YELLOW}[FIREBASE] Cleanup: Removed {doc_id} from {matched_id} matches.")
                # End of update
            except Exception as e:
                print(f"{Fore.RED}[ERROR] Error removing {doc_id}: {e}")

        # Start of update: Monitor closed status (MODIFIED)
        elif change.type == ChangeType.MODIFIED:
            current_status = data.get('status')
            ai_status = data.get('ai_status')

            if current_status == 'closed' and ai_status != 'cleanup_done':
                print(f"{Fore.BLUE}\n[EVENT] Report Closed | ID: {doc_id}")
                try:
                    # 1. Delete the vector from Qdrant to prevent ghost matches
                    qdrant_id = generate_qdrant_id(doc_id)
                    client.delete(collection_name=collection_name, points_selector=[qdrant_id])
                    print(f"{Fore.BLUE}[QDRANT] Successfully removed {doc_id} because it's closed.")

                    # 2. Reverse cleanup from other reports

                    existing_matches = data.get('matches', [])
                    for match in existing_matches:
                        matched_id = match.get('matched_report_id')
                        if matched_id:
                            matched_ref = db.collection('reports').document(matched_id)
                            matched_snap = matched_ref.get()
                            if matched_snap.exists:
                                matched_data = matched_snap.to_dict()
                                matched_matches = matched_data.get('matches', [])
                                filtered = [m for m in matched_matches if m.get('matched_report_id') != doc_id]
                                if len(filtered) != len(matched_matches):
                                    matched_ref.update({'matches': filtered})
                                    print(f"{Fore.BLUE}[FIREBASE] Cleanup: Removed closed report {doc_id} from {matched_id}.")

                    # 3. Update ai_status to prevent repeated cleanup on the same report
                    db.collection('reports').document(doc_id).update({'ai_status': 'cleanup_done'})
                    print(f"{Fore.BLUE}[FIREBASE] Marked {doc_id} as cleanup_done.")

                except Exception as e:
                    print(f"{Fore.RED}[ERROR] Failed to cleanup closed report {doc_id}: {e}")
        # --- End of update ---

        # --- Handle Addition ---
        elif change.type == ChangeType.ADDED:
            if data.get('ai_status') != 'pending':
                continue

            report_type = data.get('type')
            user_id = data.get('userId', 'unknown')
            item_dict = data.get('item', {})
            report_status = data.get('status', 'open') # Update here to fetch status for indexing

            print(f"{Fore.GREEN}\n[EVENT] New Pending Report | ID: {doc_id} | Type: {report_type} | User: {user_id}")

            try:
                db.collection('reports').document(doc_id).update({'ai_status': 'processing'})
                print(f"{Fore.GREEN}[FIREBASE] Locked document {doc_id} with status 'processing'.")

                print(f"{Fore.GREEN}[PROCESS] Extracting features and executing search...")
                matches = search_weighted_matches(item_dict, report_type, user_id, top_k=3)

                print(f"{Fore.GREEN}[PROCESS] Pushing vectors to Qdrant...")
                index_report_to_qdrant(doc_id, item_dict, report_type, user_id, report_status) # Update here to pass status for indexing

                db.collection('reports').document(doc_id).update({
                    'matches': matches,
                    'ai_status': 'completed'
                })
                print(f"{Fore.GREEN}[FIREBASE] Updated NEW report {doc_id} with {len(matches)} matches and status 'completed'.")

                if matches:
                    print(f"{Fore.GREEN}[MATCHING] Initiating symmetric update for {len(matches)} older report(s)...")
                    for match in matches:
                        matched_id = match['matched_report_id']
                        score = match['score']

                        old_doc_ref = db.collection('reports').document(matched_id)
                        old_doc = old_doc_ref.get()

                        if old_doc.exists:
                            old_data = old_doc.to_dict()
                            old_matches = old_data.get('matches', [])

                            if not any(m.get('matched_report_id') == doc_id for m in old_matches):
                                old_matches.append({
                                    "matched_report_id": doc_id,
                                    "score": score
                                })
                                old_matches = sorted(old_matches, key=lambda x: x['score'], reverse=True)
                                old_matches = old_matches[:3]

                                old_doc_ref.update({'matches': old_matches})
                                print(f"{Fore.GREEN}[FIREBASE] SYMMETRIC UPDATE: Added {doc_id} to {matched_id}.")
                            else:
                                print(f"{Fore.GREEN}[FIREBASE] Skip: {doc_id} already exists in {matched_id}.")

            except Exception as e:
                print(f"{Fore.RED}[ERROR] Failed to process {doc_id}: {e}")
                db.collection('reports').document(doc_id).update({'ai_status': 'error'})
                print(f"{Fore.RED}[FIREBASE] Reverted status of {doc_id} to 'error'.")

